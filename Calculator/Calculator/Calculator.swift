import Foundation

public class Calculator {
    public init() {}

    public func sum(_ value1: Int, _ value2: Int) -> Int {
        return value1 + value2
    }

    public func sub(_ value1: Int, _ value2: Int) -> Int {
        return value1 - value2
    }
}
